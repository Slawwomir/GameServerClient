package sample.Server;

import javafx.geometry.Pos;

public class Player {
    private Position position;

}

final class Position {
    private int x;
    private int y;

    public Position(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }
}
